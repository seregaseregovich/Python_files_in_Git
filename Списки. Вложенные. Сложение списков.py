# ПРИМЕР сложения обычных списков при помощи ZIP и генератора списков

x1 = [1, 2, 3, 4, 5]
x2 = [10, 20, 30, 40, 50]
x3 = [100, 200, 300, 400, 500]
y = [a1 + a2 + a3 for a1, a2, a3 in zip(x1, x2, x3)]
print(y)


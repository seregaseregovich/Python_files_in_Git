# Задача ниже - определение числовой последовательности
# с постоянным шагом (равным единице)

a = [1, 2, 3, 4, 6]
start = a[0]
end = a[0] + len(a)
b = all(x == y for x, y in zip(a, range(start, end)))
print(b)
